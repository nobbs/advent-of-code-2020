func Test_dotsToInt(t *testing.T) {
	type args struct {
		s string
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{
			name: "example",
			args: args{s: "...#...#."},
			want: 34,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := dotsToInt(tt.args.s); got != tt.want {
				t.Errorf("dotsToInt() = %v, want %v", got, tt.want)
			}
		})
	}
}
